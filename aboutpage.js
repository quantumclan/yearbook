let currentSlideIndex = 0;

const slidesContainerElement = document.getElementById('slidesContainer');
const totalSlideImages = slidesContainerElement.children.length;

function displaySlide(slideIndex) {
  if (slideIndex >= totalSlideImages) {
    currentSlideIndex = 0;
  } else if (slideIndex < 0) {
    currentSlideIndex = totalSlideImages - 1;
  } else {
    currentSlideIndex = slideIndex;
  }
  const slideOffset = -currentSlideIndex * 300; // match image width
  slidesContainerElement.style.transform = `translateX(${slideOffset}px)`;
}

function goToNext() {
  displaySlide(currentSlideIndex + 1);
}

function goToPrevious() {
  displaySlide(currentSlideIndex - 1);
}

